int	ft_str_is_alpha(char *str)
{
	int i;

	i = 0;
	while (str[i])
	{
		if (!(97 <= str[i] && 'z' >= str[i]) && !(65 <= str[i] && 90 >= str[i]))
			return (0);
		i++;
	}
	return (1);
}
