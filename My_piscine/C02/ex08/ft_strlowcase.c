char	*ft_strlowcase(char *str)
{
	int i;

	i = 0;
	while (str[i])
	{
		if (!('A' <= str[i] && 'Z' >= str[i]))
			str[i] = str[i];
		else
			str[i] = str[i] + 32;
		i++;
	}
	return (str);
}
