int		ft_is_prime(int nb)
{
	int i;
	int a;

	i = 1;
	a = 0;
	while (nb > i)
	{
		if ((nb % i) == 0)
			a++;
		i++;
	}
	if (a == 1)
	{
		return (a);
	}
	else
		return (0);
}
